


def s_add_square(x : int , y : int) -> int :
    return ( x+y ) * ( x+y )


def s_sub_square(x : int , y : int) -> int :
    return ( x-y ) * ( x-y )

def s_mul_square(x : int , y : int) -> int :
    return ( x*y ) * ( x*y )

def s_div(x : int , y : int) -> float :
    return ( x/y ) * ( x/y )



